package core

import (
	"encoding/binary"
)

//This is a transparent gif.
//we can manipulate the pairs of bytes marked
//as little endian uint16s to produce arbitary sized gif.
var GIF = []byte{0x47, 0x49, 0x46, 0x38, 0x39, 0x61,
	0x01, 0x00, //width
	0x01, 0x00, //height
	0x00, 0x00, 0x00, 0x21, 0xf9, 0x04, 0x01, 0x0a, 0x00, 0x01, 0x00, 0x2c, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x02, 0x02, 0x4c, 0x01, 0x00, 0x3b}

func createGif(w, h uint16) []byte {
	if w == 1 && h == 1 {
		return GIF
	}
	g := make([]byte, len(GIF))
	copy(g, GIF)
	//now overwrite the values.
	binary.LittleEndian.PutUint16(g[6:8], w)
	binary.LittleEndian.PutUint16(g[8:10], h)
	return g
}
